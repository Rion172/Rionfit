     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $user = Auth::user();

        $user->musclebuilding = $request->input('musclebuilding', false);
        $user->weight_loss = $request->input('weight_loss', false);
        $user->days = $request->input('days');

        $user->save();

        $userWeight = DB::table('weights')->where('user_id', '=', Auth::id())->pluck('userweight')->toArray();

        $chart = new UserWeight;
        $chart->labels(['Weight']);
        $chart->dataset('Gewicht', 'line', $userWeight);

        return view('choice', ['chart' => $chart])
        ->with('user', $user);
    }

